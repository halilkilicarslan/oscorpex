/**
 * Proje bilgilerini, plan aşamalarını ve agent takımını kullanarak
 * repo kökünde profesyonel bir README.md dosyası oluşturur.
 *
 * Template-based string interpolation kullanılır; AI SDK çağrısı yapılmaz.
 * Mevcut README.md varsa üzerine yazar (otomatik güncelleme senaryosu için).
 */
export async function generateReadme(
  projectId: string,
  log?: (msg: string) => void,
): Promise<void> {
  // Proje kaydını veritabanından al
  const project = await getProject(projectId);
  if (!project) {
    log?.('[readme] Proje bulunamadı, README oluşturma atlandı');
    return;
  }

  if (!project.repoPath) {
    log?.('[readme] repoPath tanımlı değil, README oluşturma atlandı');
    return;
  }

  try {
    // ---- Plan ve görev bilgilerini topla ----
    const plan = await getLatestPlan(projectId);
    const phases: Phase[] = plan ? await listPhases(plan.id) : [];

    const totalTasks = phases.reduce((sum, p) => sum + (p.tasks?.length ?? 0), 0);
    const doneTasks = phases.reduce(
      (sum, p) => sum + (p.tasks?.filter((t) => t.status === 'done').length ?? 0),
      0,
    );

    // ---- Agent takım bilgilerini topla ----
    const agents = await listProjectAgents(projectId);
    const agentCount = agents.length;

    // Rol → benzersiz agent isimlerini grupla (kısa takım özeti için)
    const roleMap = new Map<string, string[]>();
    for (const a of agents) {
      if (!roleMap.has(a.role)) roleMap.set(a.role, []);
      roleMap.get(a.role)!.push(a.name);
    }
    const teamLines = [...roleMap.entries()].map(
      ([role, names]) => `- **${capitalize(role)}**: ${names.join(', ')}`,
    );

    // ---- Tech Stack ----
    const techItems =
      project.techStack.length > 0
        ? project.techStack.map((t) => `- ${t}`)
        : ['- Not specified'];

    // ---- Dosya ağacını al (proje yapısı bölümü için) ----
    let fileTree = '';
    try {
      const tree = await gitManager.getFileTree(project.repoPath);
      const treeLines = renderTree(tree);
      // Yalnızca ilk 50 satırı göster — çok uzun README'lerden kaçın
      fileTree = treeLines.slice(0, 50).join('\n');
      if (treeLines.length > 50) fileTree += '\n... (devamı var)';
    } catch {
      fileTree = 'Dosya yapısı okunamadı.';
    }

    // ---- ARCHITECTURE.md'den özet al (varsa) ----
    let architectureSummary = '';
    try {
      const archPath = join(project.repoPath, 'docs', 'ARCHITECTURE.md');
      if (existsSync(archPath)) {
        const archContent = await readFile(archPath, 'utf-8');
        // İlk 30 satırı özetle kullan (başlık hariç)
        const archLines = archContent
          .split('\n')
          .filter((l) => !l.startsWith('# '))  // Ana başlığı atla
          .slice(0, 30)
          .join('\n')
          .trim();
        if (archLines.length > 0) {
          architectureSummary = archLines;
        }
      }
    } catch {
      // ARCHITECTURE.md yoksa atla
    }

    // ---- Aşama görevlerini madde halinde listele ----
    const phaseLines: string[] = [];
    for (const phase of phases) {
      if (!phase.tasks || phase.tasks.length === 0) continue;
      phaseLines.push(`### Phase ${phase.order}: ${phase.name}`);
      phaseLines.push('');
      for (const task of phase.tasks) {
        const check = task.status === 'done' ? 'x' : ' ';
        phaseLines.push(`- [${check}] ${task.title}`);
      }
      phaseLines.push('');
    }

    // ---- Tarih ----
    const generatedAt = new Date().toISOString().split('T')[0];

    // ---- README.md içeriğini oluştur ----
    const readmeLines = [
      `# ${project.name}`,
      '',
      `> ${project.description || 'An AI Dev Studio project.'}`,
      '',
      '---',
      '',
      '## Tech Stack',
      '',
      ...techItems,
      '',
      '## Project Structure',
      '',
      '```',
      fileTree,
      '```',
      '',
      '## Getting Started',
      '',
      '### Prerequisites',
      '',
      '- Node.js 18+',
      '- pnpm (recommended) or npm',
      '',
      '### Installation',
      '',
      '```bash',
      'pnpm install',
      '```',
      '',
      '### Running',
      '',
      '```bash',
      '# Development',
      'pnpm dev',
      '',
      '# Build',
      'pnpm build',
      '',
      '# Tests',
      'pnpm test',
      '```',
      '',
      '## Project Plan',
      '',
      `- **Total Tasks:** ${totalTasks} (${doneTasks} completed)`,
      `- **Phases:** ${phases.length}`,
      '',
      ...phaseLines,
      '## Team',
      '',
      `Built by AI Dev Studio with ${agentCount} AI agent${agentCount !== 1 ? 's' : ''}:`,
      '',
      ...teamLines,
      '',
    ];

    // Architecture bölümü yalnızca içerik varsa ekle
    if (architectureSummary) {
      readmeLines.push(
        '## Architecture',
        '',
        architectureSummary,
        '',
      );
    }

    readmeLines.push(
      '---',
      '',
      `*Generated by [AI Dev Studio](https://github.com/voltagent/voltagent) on ${generatedAt}.*`,
      '',
    );

    // ---- README.md dosyasını repo köküne yaz ----
    const readmePath = join(project.repoPath, 'README.md');
    await writeFile(readmePath, readmeLines.join('\n'), 'utf-8');
    log?.('[readme] README.md oluşturuldu');

    // ---- Git'e commit et ----
    try {
      await gitManager.commitFiles(project.repoPath, ['README.md'], 'docs: auto-generate README.md');
      log?.('[readme] README.md git commit edildi');
    } catch {
      // Commit başarısız olsa da dosya yazıldı — loglayıp devam et
      log?.('[readme] README.md yazıldı (git commit başarısız)');
    }
  } catch (err) {
    const msg = err instanceof Error ? err.message : String(err);
    log?.(`[readme] Hata: ${msg.slice(0, 200)}`);
  }
}

/** İlk harfi büyük yapar */
function capitalize(str: string): string {
  if (!str) return str;
  return str.charAt(0).toUpperCase() + str.slice(1);
}

// ---------------------------------------------------------------------------
// Docs freshness check
// ---------------------------------------------------------------------------

export interface DocFreshnessResult {
  file: string;
  status: 'filled' | 'tbd' | 'missing';
}

/** Check which docs are still TBD or missing. */
export async function checkDocsFreshness(repoPath: string): Promise<DocFreshnessResult[]> {
  const docsDir = join(repoPath, 'docs');
  const docFiles = ['PROJECT.md', 'ARCHITECTURE.md', 'CODING_STANDARDS.md', 'API_CONTRACT.md', 'CHANGELOG.md', 'DECISIONS.md'];
  const results: DocFreshnessResult[] = [];

  for (const file of docFiles) {
    const filePath = join(docsDir, file);
    if (!existsSync(filePath)) {
      results.push({ file, status: 'missing' });
      continue;
    }
    try {
      const content = await readFile(filePath, 'utf-8');
      const isTBD = content.includes('TBD') && content.trim().length < 100;
      results.push({ file, status: isTBD ? 'tbd' : 'filled' });
    } catch {
      results.push({ file, status: 'missing' });
    }
  }

  return results;
}
